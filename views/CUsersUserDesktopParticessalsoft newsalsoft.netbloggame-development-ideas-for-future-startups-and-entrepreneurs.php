                   <h2 class="h3 primary uppercase mb-4">Growth in the Global Markets </h2>

                    <p class="primary text regular mb-4">In the past, North America, Europe, and Japan were the major markets for video games. In the last decade, however, the industry has experienced rapid growth in other markets globally. People from all over the world enjoy spending their time with something that stimulates their minds. </p>

                    <p class="primary text regular mb-4">Mobile Gaming High Use</p>

                    <p class="primary text regular mb-4">Mobile gaming is the fastest-growing video gaming platform in the industry. Accessibility to mobile games is increasing as smart devices become more accessible.</p>
                    
                    <h2 class="h3 primary uppercase mb-4">Digital Distribution</h2>

                    <p class="primary text regular mb-4">Companies have been able to eliminate their middleman and packaging costs through digitalization. These cost savings can also be passed on to consumers through lower gaming costs, making video games more affordable. </p>

